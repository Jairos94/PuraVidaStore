DECLARE @contador INT = 1;
DECLARE @totalProductos INT;

-- Obtener el total de productos en la tabla temporal
SELECT @totalProductos = COUNT(1) FROM #Productos;

WHILE @contador <= @totalProductos
BEGIN
    DECLARE @PrdNombre VARCHAR(50);
    DECLARE @Categoria VARCHAR(50);
    DECLARE @IdCategoria INT;

    -- Obtener los valores de la tabla temporal
    SELECT @PrdNombre = DescripcionArticulo, 
           @Categoria = Categoria,
		   @IdCategoria = (SELECT top(1) TppId FROM [dbo].[TipoProducto] WHERE TppDescripcion = Categoria)
    FROM #Productos
    ORDER BY (SELECT NULL) -- Ordenar por cualquier columna o ninguna

    OFFSET @contador - 1 ROWS FETCH NEXT 1 ROWS ONLY; -- Obtener el producto actual

	    -- Verificar si el producto ya existe antes de insertar
    IF NOT EXISTS (SELECT 1 FROM [dbo].[Productos] WHERE PrdNombre = @PrdNombre AND PrdIdTipoProducto = @IdCategoria)
    BEGIN



	    INSERT INTO [dbo].[Productos]
           (
			   [PrdNombre]
			   ,[PrdPrecioVentaMayorista]
			   ,[PrdPrecioVentaMinorista]
			   ,[PrdCodigo]
			   ,[PrdUnidadesMinimas]
			   ,[PrdIdTipoProducto]
			   ,[PrdCodigoProvedor]
			   ,[PdrVisible]
			   ,[PdrFoto]
			   ,[PdrTieneExistencias]
		   )
     VALUES
           (@PrdNombre
           ,1
           ,1
           ,NULL
           ,0
           , (SELECT TOP(1) TppId FROM [dbo].[TipoProducto]  WHERE TppDescripcion = @Categoria)
           ,NULL
           ,''
           ,NULL
           ,1
           );





	UPDATE P
		SET P.[PrdCodigo] = CONCAT('PVS',@IdCategoria,'C',P.PrdId)
	FROM [dbo].[Productos] P
		INNER JOIN TipoProducto T ON P.[PrdIdTipoProducto] = T.[TppId] 
		WHERE 
			T.TppDescripcion = @Categoria
			AND P.PrdNombre = @PrdNombre; 
	END

		SET @contador = @contador + 1; -- Incrementar el contador
END
Go

