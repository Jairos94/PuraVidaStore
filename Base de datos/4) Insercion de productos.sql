INSERT INTO [dbo].[Movimientos]
           ([MvmIdProducto]
           ,[MvmCantidad]
           ,[MvmFecha]
           ,[MvmIdMotivoMovimiento]
           ,[MvmIdUsuario]
           ,[MvmIdBodega])
SELECT 
    P.PrdId,
    I.SumaCantidadTienda,
    GETDATE(),
    1,
    1,
    CASE
        WHEN I.Sitio = 'Tienda' THEN 1
        ELSE 2
    END
FROM [dbo].[Productos] P
INNER JOIN #Inventario I ON P.PrdNombre = I.DescripcionArticulo
INNER JOIN TipoProducto TP ON P.PrdIdTipoProducto = TP.TppId AND TP.TppDescripcion = I.Categoria
WHERE I.SumaCantidadTienda IS NOT NULL
AND NOT EXISTS (
    SELECT 1
    FROM [dbo].[Movimientos] M
    WHERE M.MvmIdProducto = P.PrdId
    AND M.MvmCantidad = I.SumaCantidadTienda
    AND M.MvmFecha = CAST(GETDATE() AS DATE)  -- Opcional: verifica la fecha si es necesario
);

